    public void cargarDatos() {
        LeerDatos datos = new LeerDatos();
        this.zapato1 = Integer.parseInt(datos.obtenerLinea(1).replace("zapato1=",""));
        this.zapato2 = Integer.parseInt(datos.obtenerLinea(2).replace("zapato2=",""));
        totalZapato1.setText(Integer.toString(zapato1));
        totalZapato2.setText(Integer.toString(zapato2));
        totalZapatos.setText(Integer.toString(zapato1+zapato2));
        totalSuelas.setText("0");
        totalPlantillas.setText("0");
        totalTrenzas.setText("0");
    }
    
