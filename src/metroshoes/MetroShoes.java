    private void trenzaAgregarOperario1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_trenzaAgregarOperario1ActionPerformed
        if(datosCargados) {
            cargarDatos();
            datosCargados = false;
        }
        activarProduccion(true);
        eventosPlantilla.append("Se ha iniciado la produccion de plantillas"+ "\n");
        eventosSuela.append("Se ha iniciado la produccion de suelas"+"\n");
        eventosTrenza.append("Se ha iniciado la produccion de las trenzas"+"\n");
        if(iniciado) {
            while(productoresTrenza < 3) {
                productorTrenza[productoresTrenza] = new ProducirTrenza(buffer, semTrenzaS, semTrenzaEM, semTrenzaB, productoresTrenza, eventosTrenza, totalTrenzas);
                productorTrenza[productoresTrenza].start();
                productoresTrenza++;
            }
            while(productoresSuela < 2) {
                productorSuela[productoresSuela] = new ProducirSuela(buffer, semSuelaS, semSuelaEM, semSuelaB, productoresSuela, eventosSuela, totalSuelas);
                productorSuela[productoresSuela].start();
                productoresSuela++;
            }
            while(productoresPlantilla < 1) {
                 productorPlantilla[productoresPlantilla] = new ProducirPlantilla(buffer, semPlantillaS, semPlantillaEM, semPlantillaB, productoresPlantilla, eventosPlantilla, totalPlantillas);
                 productorPlantilla[productoresPlantilla].start();
                 productoresPlantilla++;
            }
            iniciado = false;
            trenzaAgregarOperario5.setEnabled(true);
        } else {
            for(int i = 0; i < productoresTrenza; i++) {
                productorTrenza[i] = new ProducirTrenza(buffer, semTrenzaS, semTrenzaEM, semTrenzaB, i, eventosTrenza, totalTrenzas);
                productorTrenza[i].start();
            }
            for(int i = 0; i < productoresSuela; i++) {
                productorSuela[i] = new ProducirSuela(buffer, semSuelaS, semSuelaEM, semSuelaB, i, eventosSuela, totalSuelas);
                productorSuela[i].start();
            }
            for(int i = 0; i < productoresPlantilla; i++) {
                productorPlantilla[i] = new ProducirPlantilla(buffer, semPlantillaS, semPlantillaEM, semPlantillaB, i, eventosPlantilla, totalPlantillas);
                productorPlantilla[i].start();
            }
        }
        trenzaEliminarOperario1.setEnabled(true);
        trenzaEliminarOperario1.setVisible(true);
        trenzaAgregarOperario1.setEnabled(false);
        trenzaAgregarOperario1.setVisible(false);
        trenzasActualizarTotalOperadores();
        suelasActualizarTotalOperadores();
        plantillasActualizarTotalOperadores();
    }//GEN-LAST:event_trenzaAgregarOperario1ActionPerformed

    private void trenzaAgregarOperario5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_trenzaAgregarOperario5ActionPerformed
        if(datosCargados) {
            cargarDatos();
            datosCargados = false;
        }
        activarElaboracion(true);
        eventosElaboradores.append("Se ha iniciado la elaboración"+"\n");
        if (iniciarElaboracion) {
            if(operadoresElaboradores == 0) {
                consumidor[operadoresElaboradores] = new ElaborarZapato(semTrenzaS, semTrenzaEM, semTrenzaB, semSuelaS, semSuelaEM, semSuelaB, semPlantillaS, semPlantillaEM, semPlantillaB, buffer, buffer, buffer, zapato1, zapato2, operadoresElaboradores, eventosElaboradores, zapatosTipoI, zapatosTipoII, totalTrenzas, totalSuelas, totalPlantillas);
                consumidor[operadoresElaboradores].start();
                operadoresElaboradores++;
            }
            iniciarElaboracion = false;
        } else {
            for (int i = 0; i < operadoresElaboradores; i++) {
                consumidor[i] = new ElaborarZapato(semTrenzaS, semTrenzaEM, semTrenzaB, semSuelaS, semSuelaEM, semSuelaB, semPlantillaS, semPlantillaEM, semPlantillaB, buffer, buffer, buffer, zapato1, zapato2, i, eventosElaboradores, zapatosTipoI, zapatosTipoII, totalTrenzas, totalSuelas, totalPlantillas);
                consumidor[i].start();
            }
        }
        trenzaEliminarOperario5.setEnabled(true);
        trenzaEliminarOperario5.setVisible(true);
        trenzaAgregarOperario5.setEnabled(false);
        trenzaAgregarOperario5.setVisible(false);
        actualizarOperadoresElaboradores();
    }//GEN-LAST:event_trenzaAgregarOperario5ActionPerformed

